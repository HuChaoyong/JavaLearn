package Chapter11;

// 简单介绍 Math 类

public class MathDemo1 {
    public static void main(String[] args) {
        System.out.println("9开平方 = " + Math.sqrt(9.0));
        System.out.println("2 和 3求大者 = " + Math.max(2, 3));
        System.out.println("2 和 3 求小者  = " + Math.min(2, 3));
        System.out.println("2的3次方 = " + Math.pow(2, 3));
        System.out.println("2.3 四舍五入 = " + Math.round(2.3));
    }
}
